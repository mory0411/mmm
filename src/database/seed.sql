-- 더미 관계 데이터 생성 (hash_code 포함)
INSERT INTO relationships (hash_code)
SELECT encode(sha256((gen_random_uuid()::text)::bytea), 'hex')
FROM generate_series(1, 10);

-- 더미 answers 데이터 생성
INSERT INTO answers (relationship_id, question_id, answer_text, role)
SELECT 
    r.id,
    q.id,
    CASE 
        WHEN role = 'child' THEN '자녀의 답변: ' || 
            CASE q.text
                WHEN '오늘 하루 중 가장 기억에 남는 순간은 무엇인가요?' THEN '부모님과 함께한 저녁 식사 시간이에요. 오랜만에 대화를 많이 나눴어요.'
                WHEN '어린 시절 가장 재미있었던 놀이는 무엇인가요?' THEN '아빠와 함께했던 숨바꼭질이요. 항상 같은 곳에 숨었는데도 못 찾은 척 해주셨죠.'
                WHEN '우리 가족만의 특별한 전통이나 규칙이 있나요?' THEN '일요일 아침에는 항상 다 같이 브런치를 먹어요.'
                WHEN '지금까지 가장 행복했던 가족 여행은 언제인가요?' THEN '작년 여름 제주도 여행이요. 처음으로 비행기를 타고 가족여행을 갔어요.'
                WHEN '부모님/자녀에게 가장 감사한 점은 무엇인가요?' THEN '항상 제 선택을 존중해주시고 지지해주셔서 감사해요.'
                WHEN '우리 가족의 장점은 무엇이라고 생각하나요?' THEN '서로를 믿고 이해하려 노력하는 점이요.'
                WHEN '가족과 함께 하고 싶은 버킷리스트가 있나요?' THEN '세계 일주를 하면서 다양한 추억을 만들고 싶어요.'
                WHEN '우리 가족의 첫 만남을 기억하시나요?' THEN '제가 태어났을 때 이야기를 들었는데, 정말 감동적이에요.'
                WHEN '가족에게 전하지 못한 마음이 있다면 무엇인가요?' THEN '항상 사랑한다고 말하고 싶었어요.'
                ELSE '우리 가족이 건강하고 행복했으면 좋겠어요.'
            END
        ELSE '부모의 답변: ' || 
            CASE q.text
                WHEN '오늘 하루 중 가장 기억에 남는 순간은 무엇인가요?' THEN '아이와 함께 이야기를 나누며 웃었던 순간이에요.'
                WHEN '어린 시절 가장 재미있었던 놀이는 무엇인가요?' THEN '아이와 함께 했던 모든 놀이가 특별했어요.'
                WHEN '우리 가족만의 특별한 전통이나 규칙이 있나요?' THEN '매주 일요일 아침은 가족과 함께하는 시간으로 정했어요.'
                WHEN '지금까지 가장 행복했던 가족 여행은 언제인가요?' THEN '작년 여름 제주도 여행에서 아이의 행복한 모습을 봤을 때에요.'
                WHEN '부모님/자녀에게 가장 감사한 점은 무엇인가요?' THEN '매일 새로운 기쁨을 주는 우리 아이에게 감사해요.'
                WHEN '우리 가족의 장점은 무엇이라고 생각하나요?' THEN '서로의 이야기를 경청하고 이해하려 노력하는 점이에요.'
                WHEN '가족과 함께 하고 싶은 버킷리스트가 있나요?' THEN '아이와 함께 세계 여러 나라를 여행하며 견문을 넓히고 싶어요.'
                WHEN '우리 가족의 첫 만남을 기억하시나요?' THEN '아이가 태어났을 때의 그 감동을 잊을 수 없어요.'
                WHEN '가족에게 전하지 못한 마음이 있다면 무엇인가요?' THEN '항상 자랑스럽고 사랑한다고 말하고 싶어요.'
                ELSE '우리 가족이 더욱 끈끈하고 행복한 추억을 많이 만들었으면 좋겠어요.'
            END
    END,
    role::text
FROM relationships r
CROSS JOIN questions q
CROSS JOIN (VALUES ('child'), ('parent')) AS roles(role); 